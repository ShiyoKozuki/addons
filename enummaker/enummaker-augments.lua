 local function CleanText(augment)
    local cleanString = string.upper(augment.stat:gsub('[%p%c%s]', ''));
    if augment.multiplier and (augment.multiplier < 1) then
        cleanString = cleanString .. 'MINUS';
    end
    if augment.offset and (augment.offset > 1) then
        cleanString = cleanString .. augment.offset;
    end 
    return cleanString;
 end

 local function writeAugment(file, id, augment)
    for _,single in pairs(augment) do
        if (type(single) ~= 'table') then
          return;
        end
        file:write(CleanText(single));
    end
    file:write(string.format(' = %u,\n', id));
 end

